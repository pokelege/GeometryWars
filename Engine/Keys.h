#ifndef KEYS
#define KEYS
/*
* VK_0 - VK_9 are the same as ASCII '0' - '9' (0x30 - 0x39)
* 0x40 : unassigned
* VK_A - VK_Z are the same as ASCII 'A' - 'Z' (0x41 - 0x5A)
*/
#define VK_A 0x41
#define VK_B 0x42

#define VK_D 0x44

#define VK_N 0x4E

#define VK_R 0x52
#define VK_S 0x53

#define VK_V 0x56
#define VK_W 0x57
#endif